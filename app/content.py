def _pick(items: list[str]) -> str:
    return random.choice(items)


def _pick_unique(items: list[str], count: int) -> list[str]:
    pool = list(items)
    random.shuffle(pool)
    if count >= len(pool):
        return pool[:count]
    return pool[:count]


def _hashtags(niche: str) -> str:
    tag = niche.replace(" ", "").replace("-", "")
    return f"#{tag} {_pick(HASHTAG_SUFFIXES)}"


def _generate_offline(content_type: ContentType, niche: str) -> str:
    if content_type == ContentType.HOOKS:
        hooks = _pick_unique(HOOK_TEMPLATES, 5)
        lines = [h.format(niche=niche) for h in hooks]
        return "🪝 5 хуков для твоего контента:\n\n" + "\n\n".join(
            f"{i}. {h}" for i, h in enumerate(lines, 1)
        )

    if content_type == ContentType.PLAN:
        tasks = _pick_unique(PLAN_TASKS, 7)
        lines = [f"День {i + 1}: {t.format(niche=niche)}" for i, t in enumerate(tasks)]
        return (
            f"📅 Контент-план на 7 дней для ниши «{niche}»:\n\n"
            + "\n".join(lines)
            + "\n\n💡 Публикуй в одно время — так алгоритм быстрее запомнит тебя."
        )

    if content_type == ContentType.CAPTION:
        hook = _pick(HOOK_TEMPLATES).format(niche=niche)
        template = _pick(CAPTION_TEMPLATES)
        body = template.format(
            hook=hook,
            niche=niche,
            insight=_pick(INSIGHTS),
            action=_pick(STEPS).lower(),
        )
        return f"✍️ Подпись для поста в нише «{niche}»:\n\n{body}\n\n{_hashtags(niche)}"

    hook = _pick(HOOK_TEMPLATES).format(niche=niche)
    template = _pick(POST_STRUCTURES)
    steps = _pick_unique(STEPS, 4)
    return template.format(
        hook=hook,
        niche=niche,
        myth=_pick(MYTHS),
        truth=_pick(TRUTHS),
        step1=steps[0],
        step2=steps[1] if len(steps) > 1 else _pick(STEPS),
        step3=steps[2] if len(steps) > 2 else _pick(STEPS),
        step4=steps[3] if len(steps) > 3 else _pick(STEPS),
        dont=_pick(MYTHS),
        do=_pick(TRUTHS),
        reason=_pick(REASONS),
        cta=_pick(CTAS),
    )


async def generate_content(
    settings: Settings,
    content_type: ContentType,
    niche: str,
    extra: str | None = None,
) -> str:
    niche = niche.strip() or "бизнес"
    if not settings.openai_api_key:
        return _generate_offline(content_type, niche)

    prompts = {
        ContentType.POST: (
            f"Напиши готовый пост для соцсетей в нише «{niche}». "
            f"Стиль: живой, без воды, с призывом к действию. "
            f"Дополнительно: {extra or 'без уточнений'}."
        ),
        ContentType.HOOKS: (
            f"Дай 5 цепляющих хуков для контента в нише «{niche}». "
            f"Каждый с новой строки, пронумерованный."
        ),
        ContentType.PLAN: (
            f"Составь контент-план на 7 дней для ниши «{niche}». "
            f"Каждый день — конкретная тема и формат."
        ),
        ContentType.CAPTION: (
            f"Напиши подпись к посту в нише «{niche}» с хештегами. "
            f"Дополнительно: {extra or 'без уточнений'}."
        ),
    }

    client = AsyncOpenAI(api_key=settings.openai_api_key)
    response = await client.chat.completions.create(
        model=settings.openai_model,
        messages=[
            {
                "role": "system",
                "content": (
                    "Ты копирайтер для соцсетей. Пиши на русском, кратко и по делу. "
                    "Без markdown-заголовков."
                ),
            },
            {"role": "user", "content": prompts[content_type]},
        ],
        temperature=0.9,
        max_tokens=900,
    )
    text = (response.choices[0].message.content or "").strip()
    return text or _generate_offline(content_type, niche)
