def detect_niche_pack(niche: str) -> NichePack | None:
    normalized = niche.lower().strip()
    for pack in NICHE_PACKS:
        if any(keyword in normalized for keyword in pack.keywords):
            return pack
    return None


def _blend(generic: list[str], specific: tuple[str, ...], niche_ratio: float = 0.65) -> list[str]:
    if not specific:
        return generic
    niche_items = list(specific)
    generic_items = list(generic)
    random.shuffle(niche_items)
    random.shuffle(generic_items)
    niche_count = max(1, int(len(generic) * niche_ratio))
    blended = niche_items[:niche_count]
    for item in generic_items:
        if item not in blended:
            blended.append(item)
    return blended
